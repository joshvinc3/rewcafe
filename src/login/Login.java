        public static boolean loginAcc(String username, String password){
        dbConnector connector = new dbConnector();
        
        
        try{
            String query = "SELECT * FROM tbl_members  WHERE u_username = '" + username + "'";
            ResultSet resultSet = connector.getData(query);
           if(resultSet.next()){
   
             String hashedPass = resultSet.getString("u_password");
             String rehashedPass = passwordHasher.hashPassword(password);
         
              if(hashedPass.equals(rehashedPass)){
                
            status = resultSet.getString("u_status");
              type = resultSet.getString("u_type");
              
             Session sess = Session.getInstance();
             sess.setUid(resultSet.getInt("u_id"));
             sess.setFnam(resultSet.getString("u_fname")); 
             sess.setLnam(resultSet.getString("u_lname"));
             sess.setUemail(resultSet.getString("u_email"));
             sess.setUsernam(resultSet.getString("u_username"));
             sess.setTyp(resultSet.getString("u_type"));  
             sess.setStat(resultSet.getString("u_status"));
      
               return true;
     }else{
       return false;      
              }
       }else{ 
           return false;
     }

        }catch (SQLException |NoSuchAlgorithmException ex) {
            return false;           
   }
}
        

